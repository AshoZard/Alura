let num1 = parseFloat(prompt("Insira o 1º número"));
let num2 = parseFloat(prompt("Insira o 2º número"));

let soma = num1 + num2;
let subtracao = num1 - num2;
let multiplicacao = num1 * num2;
let divisao = num1 / num2;

alert(
  "Soma: " +
    soma +
    "\n" +
    "Subtração: " +
    subtracao +
    "\n" +
    "Multiplicação: " +
    multiplicacao +
    "\n" +
    "Divisão: " +
    divisao
);
